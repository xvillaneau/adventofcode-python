def parse_line(line):
    px, py, vx, vy = RE_LINE.search(line).groups()
    return [int(px), int(py)], [int(vx), int(vy)]


def parse_lines(lines):
    positions, velocities = [], []
    for l in lines:
        p, v = parse_line(l)
        positions.append(p)
        velocities.append(v)
    return np.array(positions, dtype=int), np.array(velocities, dtype=int)


