from aoc_2020.day_20 import *

import numpy as np


def test_border_id():
    assert border_id(np.array([0, 0, 1, 1, 0, 1, 0, 0, 1, 0])) == 0b0011010010
    assert border_id(np.array([1, 0, 0, 0, 1, 1, 0, 1, 0, 0])) == 0b0010110001


def test_read_tile():
    tile = TileData.parse(EXAMPLE.splitlines()[:11])
    assert tile.sides == (0xd2, 0x13e, 0xe7, 0x59)


def test_find_corners():
    tiles = parse_tiles(EXAMPLE.splitlines())
    corners = {id for id, tile in tiles.items() if tile.is_corner}
    assert corners == {1951, 3079, 2971, 1171}


def test_assemble_tiles():
    tiles = parse_tiles(EXAMPLE.splitlines())
    expected = np.array([list(ln) for ln in ASSEMBLED.splitlines()]) == "#"
    assembled = assemble_tiles(tiles)
    assert any(np.all(assembled == mat) for mat in configurations(expected))


def test_water_roughness():
    for _ in range(1000):
        tiles = parse_tiles(EXAMPLE.splitlines())
        assembled = assemble_tiles(tiles)
        assert water_roughness(assembled) == 273


